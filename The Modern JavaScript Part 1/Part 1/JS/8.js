// Basic Example of Arrow Functions

let sum = (a, b) => a + b;
console.log(sum(5,4));

// If there is only ONE argument paranthesis can be omitted

let number = n => n;
console.log(number(5));

// If there are no arguments , Still paranthesis are required

let user = () => 'Karan Singh Negi';
console.log(user());



